"""
pcontext.core.constants — Константы приложения PContext.

Здесь собраны ВСЕ «магические» значения приложения:
  • Имя и версия
  • Пути к директориям и файлам
  • Настройки IPC (межпроцессное взаимодействие)
  • Расширения файлов по типам (Image, Video, TXT, ...)
  • Флаги текущей платформы (Windows / Linux)

Все константы помечены как Final — это подсказка для Pylance и mypy,
что значение нельзя переприсваивать после инициализации.
"""

from __future__ import annotations

import platform
from pathlib import Path
from typing import Final


# ═════════════════════════════════════════════════════════════════════════════
# Общие константы приложения
# ═════════════════════════════════════════════════════════════════════════════

# Название приложения.
# Используется в: заголовках окон, ключах реестра, tray-иконке, логах.
APP_NAME: Final[str] = "PContext"

# Версия приложения.
# Единый источник правды — отсюда значение попадает в __init__.__version__.
APP_VERSION: Final[str] = "0.1.0"


# ═════════════════════════════════════════════════════════════════════════════
# Пути файловой системы
# ═════════════════════════════════════════════════════════════════════════════
#
# Все данные PContext хранятся в папке ~/.pcontext/ внутри домашней
# директории пользователя:
#   Windows:  C:\Users\<имя_пользователя>\.pcontext\
#   Linux:    /home/<имя_пользователя>/.pcontext/
#

# Корневая папка данных PContext
PCONTEXT_HOME: Final[Path] = Path.home() / ".pcontext"

# ---- Скрипты ----

# Папка, куда пользователь помещает свои .py-скрипты.
# Подпапки внутри неё автоматически становятся группами в контекстном меню.
# Пример: scripts/Analyze/analyze.py → PContext → Analyze → <название>
SCRIPTS_DIR: Final[Path] = PCONTEXT_HOME / "scripts"

# ---- Виртуальное окружение ----

# Папка с виртуальным окружением Python (venv).
# Сюда устанавливаются pip-зависимости, объявленные в скриптах.
# Все скрипты делят одно окружение (конфликты — на совести пользователя).
VENV_DIR: Final[Path] = PCONTEXT_HOME / "venv"

# ---- Конфигурация ----

# Папка для конфигурационных файлов (JSON)
CONFIG_DIR: Final[Path] = PCONTEXT_HOME / "config"

# Настройки приложения: автозапуск, уведомления и прочее.
# Формат JSON:
#   {
#     "autostart": false,
#     "notifications_enabled": true
#   }
SETTINGS_FILE: Final[Path] = CONFIG_DIR / "settings.json"

# Сохранённые значения параметров (Param) каждого скрипта.
# Формат JSON:
#   {
#     "script_id_1": {"param_a": "value", "param_b": 42},
#     "script_id_2": {"model_name": "yolov8n.pt"}
#   }
PARAMS_FILE: Final[Path] = CONFIG_DIR / "params.json"

# Кеш метаданных скриптов.
# Хранит хеш файла и время последней регистрации, чтобы при повторной
# регистрации не обрабатывать неизменённые скрипты заново.
# Формат JSON:
#   {
#     "script_id": {
#       "file_path": "/home/user/.pcontext/scripts/upload.py",
#       "file_hash": "sha256:abc123...",
#       "registered_at": "2024-01-15T10:30:00"
#     }
#   }
SCRIPTS_CACHE_FILE: Final[Path] = CONFIG_DIR / "scripts_cache.json"

# ---- Логи ----

# Папка для файлов логирования
LOGS_DIR: Final[Path] = PCONTEXT_HOME / "logs"

# База данных SQLite с историей запусков скриптов.
# Таблица runs: id, script_id, started_at, duration_ms, status, result_json
RUNS_DB_FILE: Final[Path] = LOGS_DIR / "runs.db"

# ---- Кеш ----

# Папка для временных файлов (например, промежуточные результаты скриптов)
CACHE_DIR: Final[Path] = PCONTEXT_HOME / "cache"


# ═════════════════════════════════════════════════════════════════════════════
# Настройки IPC (межпроцессная коммуникация)
# ═════════════════════════════════════════════════════════════════════════════
#
# Когда пользователь кликает пункт в контекстном меню, Windows/Linux
# запускает лёгкий процесс (invoke.py), который по TCP отправляет
# команду основному процессу PContext (IPC-серверу).
#

# Адрес IPC-сервера — только localhost (не принимаем внешние подключения)
IPC_HOST: Final[str] = "127.0.0.1"

# Порт IPC-сервера.
# Выбран из диапазона >10000, чтобы не конфликтовать со стандартными портами.
IPC_PORT: Final[int] = 19836

# Максимальный размер одного IPC-сообщения в байтах (1 МБ).
# Защита от случайной или злонамеренной отправки гигантского сообщения.
IPC_MAX_MESSAGE_SIZE: Final[int] = 1024 * 1024

# Таймаут ожидания ответа от IPC-сервера (в секундах).
# Если основной процесс PContext не отвечает за это время,
# invoke.py завершается с ошибкой.
IPC_TIMEOUT: Final[float] = 5.0

# Задержка агрегации при множественном выборе файлов (в секундах).
#
# Проблема: при выборе нескольких файлов Windows вызывает команду
# контекстного меню ОТДЕЛЬНО для каждого файла. Чтобы скрипт получил
# все файлы одним списком, IPC-сервер собирает сообщения в «пакет»
# (batch) и ждёт эту задержку после получения последнего файла.
# Если за это время новых файлов не поступило, пакет считается полным.
BATCH_AGGREGATION_DELAY: Final[float] = 0.5


# ═════════════════════════════════════════════════════════════════════════════
# Расширения файлов по типам
# ═════════════════════════════════════════════════════════════════════════════
#
# Каждый тип файла (Image, Video, ...) определяется набором расширений.
# Расширения указаны в нижнем регистре, с точкой в начале: ".jpg", ".png".
#
# Используются frozenset — неизменяемые множества:
#   • Нельзя случайно изменить набор расширений
#   • Быстрая проверка «есть ли расширение в наборе» — O(1)
#

# Изображения — форматы, которые можно открыть в просмотрщике
IMAGE_EXTENSIONS: Final[frozenset[str]] = frozenset({
    ".jpg", ".jpeg", ".png", ".gif", ".bmp",
    ".tiff", ".tif", ".webp", ".svg", ".ico",
    ".heic", ".heif", ".avif",
})

# Видеофайлы — форматы, которые можно открыть в видеоплеере
VIDEO_EXTENSIONS: Final[frozenset[str]] = frozenset({
    ".mp4", ".avi", ".mkv", ".mov", ".wmv",
    ".flv", ".webm", ".m4v", ".mpg", ".mpeg",
    ".3gp",
})

# Текстовые файлы — всё, что можно открыть и прочитать в Блокноте.
# Включает: plain text, разметку, конфигурации, скрипты оболочки,
# веб-файлы (HTML/CSS/JS).
TXT_EXTENSIONS: Final[frozenset[str]] = frozenset({
    # Обычный текст и разметка
    ".txt", ".log", ".md", ".rst",
    # Структурированные данные
    ".yaml", ".yml", ".json", ".xml", ".toml",
    # Табличные данные
    ".csv", ".tsv",
    # Конфигурационные файлы
    ".ini", ".cfg", ".conf", ".env",
    # Скрипты оболочки
    ".sh", ".bat", ".ps1",
    # Веб
    ".html", ".htm", ".css",
    ".js", ".jsx", ".ts", ".tsx",
})

# Документы — форматы офисных приложений и PDF
DOC_EXTENSIONS: Final[frozenset[str]] = frozenset({
    # PDF
    ".pdf",
    # Microsoft Office
    ".doc", ".docx", ".xls", ".xlsx", ".ppt", ".pptx",
    # LibreOffice / OpenDocument
    ".odt", ".ods", ".odp",
    # Прочие
    ".rtf",
})

# Архивы — форматы сжатия и архивации
ARCHIVE_EXTENSIONS: Final[frozenset[str]] = frozenset({
    ".zip", ".7z", ".rar",
    ".tar", ".gz", ".bz2", ".xz", ".tgz",
})

# Python-файлы
PYTHON_EXTENSIONS: Final[frozenset[str]] = frozenset({
    ".py",   # обычный Python-скрипт
    ".pyw",  # скрипт без консоли (Windows)
    ".pyi",  # файл аннотаций типов (type stub)
})

# Исполняемые файлы (преимущественно Windows)
EXE_EXTENSIONS: Final[frozenset[str]] = frozenset({
    ".exe",  # основной формат исполняемых файлов Windows
    ".msi",  # установщик Windows
    ".bat",  # пакетный файл
    ".cmd",  # командный файл
    ".com",  # устаревший формат DOS
    ".scr",  # скринсейвер
})

# Словарь: название типа → набор его расширений.
# Ключи соответствуют именам классов из pcontext.api.file_types
# (будут созданы в Этапе 2).
#
# Специальные типы «File» (любой файл) и «Folder» (папка) здесь
# не представлены, т.к. они не ограничены расширениями.
FILE_TYPE_EXTENSIONS: Final[dict[str, frozenset[str]]] = {
    "Image":   IMAGE_EXTENSIONS,
    "Video":   VIDEO_EXTENSIONS,
    "TXT":     TXT_EXTENSIONS,
    "DOC":     DOC_EXTENSIONS,
    "ARCHIVE": ARCHIVE_EXTENSIONS,
    "PYTHON":  PYTHON_EXTENSIONS,
    "EXE":     EXE_EXTENSIONS,
}


# ═════════════════════════════════════════════════════════════════════════════
# Определение текущей платформы
# ═════════════════════════════════════════════════════════════════════════════

# Строковое имя ОС: "Windows", "Linux" или "Darwin" (macOS)
CURRENT_PLATFORM: Final[str] = platform.system()

# Удобные булевы флаги для проверки платформы в коде.
# Пример использования:
#   if IS_WINDOWS:
#       import winreg
IS_WINDOWS: Final[bool] = CURRENT_PLATFORM == "Windows"
IS_LINUX: Final[bool] = CURRENT_PLATFORM == "Linux"